/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */
    usb_cdc_tx_buff[0] = USB_SOF; // Start of USB frame
    usb_cdc_tx_buff[USB_CDC_TX_BUFF_LEN - 1] = USB_EOF; // End of USB frame

  /* USER CODE END 1 */
/* USER CODE BEGIN Boot_Mode_Sequence_0 */
#if defined(DUAL_CORE_BOOT_SYNC_SEQUENCE)
  int32_t timeout;
#endif /* DUAL_CORE_BOOT_SYNC_SEQUENCE */
/* USER CODE END Boot_Mode_Sequence_0 */

/* USER CODE BEGIN Boot_Mode_Sequence_1 */
#if defined(DUAL_CORE_BOOT_SYNC_SEQUENCE)
  /* Wait until CPU2 boots and enters in stop mode or timeout*/
  timeout = 0xFFFF;
  while((__HAL_RCC_GET_FLAG(RCC_FLAG_D2CKRDY) != RESET) && (timeout-- > 0));
  if ( timeout < 0 )
  {
  Error_Handler();
  }
#endif /* DUAL_CORE_BOOT_SYNC_SEQUENCE */
/* USER CODE END Boot_Mode_Sequence_1 */
  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();
/* USER CODE BEGIN Boot_Mode_Sequence_2 */
#if defined(DUAL_CORE_BOOT_SYNC_SEQUENCE)
/* When system initialization is finished, Cortex-M7 will release Cortex-M4 by means of
HSEM notification */
/*HW semaphore Clock enable*/
__HAL_RCC_HSEM_CLK_ENABLE();
/*Take HSEM */
HAL_HSEM_FastTake(HSEM_ID_0);
/*Release HSEM in order to notify the CPU2(CM4)*/
HAL_HSEM_Release(HSEM_ID_0,0);
/* wait until CPU2 wakes up from stop mode */
timeout = 0xFFFF;
while((__HAL_RCC_GET_FLAG(RCC_FLAG_D2CKRDY) == RESET) && (timeout-- > 0));
if ( timeout < 0 )
{
Error_Handler();
}
#endif /* DUAL_CORE_BOOT_SYNC_SEQUENCE */
/* USER CODE END Boot_Mode_Sequence_2 */

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_DFSDM1_Init();
  MX_USB_DEVICE_Init();
  /* USER CODE BEGIN 2 */
    HAL_DFSDM_FilterRegularStart_DMA(&hdfsdm1_filter0, (int32_t *)pcm_data, PCM_DATA_LEN);
  /* USER CODE END 2 */

  /* Initialize leds */
  BSP_LED_Init(LED_GREEN);
  BSP_LED_Init(LED_YELLOW);
  BSP_LED_Init(LED_RED);

  /* Initialize USER push-button, will be used to trigger an interrupt each time it's pressed.*/
  BSP_PB_Init(BUTTON_USER, BUTTON_MODE_EXTI);

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    switch(dfsdm_state)
    {
        case DFSDM_TOP_CPLT:
        {
            usb_cdc_tx_buff[1] = HAL_GetTick(); // Time stamp
            // Right shift each PCM data element by 8 bits
            for(int i = 0; i < PCM_DATA_LEN/2; i++)
            {
                usb_cdc_tx_buff[i + 2] = pcm_data[i] >> 8;
            }
            if(CDC_Transmit_FS((uint8_t *)usb_cdc_tx_buff, USB_CDC_TX_BUFF_SIZE) != USBD_OK)
            {
                usb_drop++;
                BSP_LED_Off(LED_YELLOW);
            }
            else
            {
                BSP_LED_Toggle(LED_YELLOW);
            }
            dfsdm_state = DFSDM_FED; // Wait for next half of data
            break;
        }
        case DFSDM_BOT_CPLT:
        {
            usb_cdc_tx_buff[1] = HAL_GetTick(); // Time stamp
            // Right shift each PCM data element by 8 bits
            for(int i = 0; i < PCM_DATA_LEN/2; i++)
            {
                usb_cdc_tx_buff[i + 2] = pcm_data[i + PCM_DATA_LEN/2] >> 8;
            }
            if(CDC_Transmit_FS((uint8_t *)usb_cdc_tx_buff, USB_CDC_TX_BUFF_SIZE) != USBD_OK)
            {
                usb_drop++;
                BSP_LED_Off(LED_YELLOW);
            }
            else
            {
                BSP_LED_Toggle(LED_YELLOW);
            }
            dfsdm_state = DFSDM_FED; // Wait for next half of data
            break;
        }
        default:
        {
            // Do nothing, wait for next data
            break;
        }
    }
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

